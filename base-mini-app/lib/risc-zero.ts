/**
 * Mock proof generation (for demo/development only)
 *
 * **DELETE THIS FUNCTION** when integrating real RISC Zero SDK
 *
 * In production, this will:
 * 1. Serialize inputs for guest program
 * 2. Execute RISC Zero zkVM with guest code
 * 3. Generate STARK proof (takes ~2-5 seconds)
 * 4. Extract proof bytes and public inputs
 * 5. Return formatted proof for contract verification
 */
function generateMockProof(inputs: BeliefProofInputs): ZKProof {
  // Generate deterministic "proof" based on inputs (for testing consistency)
  const inputHash = keccak256(
    toBytes(
      `${inputs.beliefHash}${inputs.loyaltyScore}${inputs.moduleId}${inputs.activityProof}`
    )
  );

  // Mock proof bytes (128 bytes)
  // In production, this would be the actual STARK proof from RISC Zero
  const mockProofData = new Uint8Array(128);
  const hashBytes = toBytes(inputHash);

  // Fill with deterministic data based on input hash
  for (let i = 0; i < 128; i++) {
    mockProofData[i] = hashBytes[i % hashBytes.length];
  }

  const proofBytes = bytesToHex(mockProofData);

  // Public inputs (what the verifier sees on-chain)
  // These are NOT private and will be visible on Basescan
  const publicInputs = [
    BigInt(inputs.beliefHash), // Belief hash (public)
    BigInt(inputs.moduleId),   // Module ID (public)
    // Loyalty score is PRIVATE (hidden in ZK proof)
    // Activity proof is PRIVATE (hidden in ZK proof)
  ];

  return {
    proofBytes,
    publicInputs,
  };
}

